#Definimos una tupla
cocina = ("cuchara", "cuchillo", "tenedor")
print(len(cocina))

#Acceder a un elemento, para este utilizamos corchetes no parentesis
print(cocina[0])

#Mostrar de manera inversa
print(cocina[-1])

# Acceder a un rango
print(cocina[0:1])

